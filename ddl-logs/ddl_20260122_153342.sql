CREATE TABLE d_dic_directory (id INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY, name VARCHAR(100) NOT NULL, order_string VARCHAR(25), parent_id INTEGER, create_time TIMESTAMP, creator VARCHAR(32), modifier VARCHAR(32), update_time TIMESTAMP, delete_flag NUMERIC(1,0) DEFAULT 0, PRIMARY KEY ( id ))
COMMENT ON TABLE d_dic_directory IS '字典目录'
COMMENT ON COLUMN d_dic_directory.id IS '主键ID'
COMMENT ON COLUMN d_dic_directory.name IS '目录名'
COMMENT ON COLUMN d_dic_directory.create_time IS '创建时间'
COMMENT ON COLUMN d_dic_directory.creator IS '创建人'
COMMENT ON COLUMN d_dic_directory.modifier IS '修改人'
COMMENT ON COLUMN d_dic_directory.update_time IS '修改时间'
COMMENT ON COLUMN d_dic_directory.delete_flag IS '逻辑删除'
